package com.revature.Day9.ExerciseHashTree;

import java.util.TreeSet;

public class Tree1 {
    public static void main(String[] args) {
        TreeSet<String> set = new TreeSet<String>();
        set.add("Red");
        set.add("Green");
        set.add("Orange");
        set.add("White");
        set.add("Black");
        System.out.println("Tree set: ");
        System.out.println(set);
    }
}
